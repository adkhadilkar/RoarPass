# ----------------------------- support stubs --------------------------------
# These wrap real tooling Claude Code fills in on the machine (headless browser, package mgrs).
# They are isolated so the orchestration logic above stays clean and testable.

def _git_init_if_needed():
    if not (PRODUCT / ".git").exists():
        import subprocess
        subprocess.run(["git", "init"], cwd=PRODUCT, capture_output=True)
        subprocess.run(["git", "branch", "-M", "main"], cwd=PRODUCT, capture_output=True)


def _git_msg(key: str, **kw) -> str:
    tmpl = yaml.safe_load((_ROOT / "config/git.yaml").read_text())["commit_policy"][key]
    return tmpl.format(**kw) if kw else tmpl


def _git_branch(key: str) -> str:
    return yaml.safe_load((_ROOT / "config/git.yaml").read_text())["branching"][key]


def _assemble_refined_requirements(chunks):
    parts = ["# RoarPass — Refined Requirements\n\n## Traceability\n",
             "| Chunk | PRD refs | Spec |\n|---|---|---|"]
    for c in chunks:
        parts.append(f"| {c['title']} | {', '.join(c.get('prd_refs', []))} | "
                     f"`docs/requirements/specs/{c['slug']}.md` |")
    parts.append("\n---\n")
    for c in chunks:
        sp = PRODUCT / f"docs/requirements/specs/{c['slug']}.md"
        if sp.exists():
            parts.append(f"\n# {c['title']}\n\n" + sp.read_text())
    (PRODUCT / "docs/requirements/REFINED_REQUIREMENTS.md").write_text("\n".join(parts))


def _assemble_design_readme(pages):
    parts = ["# RoarPass — Design Walkthrough (for your approval)\n",
             "Web + mobile-web designs for every feature. Screens are in `screens/`.\n",
             "## What we're asking you to approve\nThe IA, visual system, and per-page layouts "
             "below. Approve to proceed to implementation, or request changes.\n\n---\n"]
    for p in pages:
        slug = p["slug"]
        parts.append(f"## {p['title']}\n")
        parts.append(f"_Requirements: {', '.join(p.get('req_refs', []))}_\n")
        parts.append(f"![{slug} desktop](screens/{slug}-desktop.png)\n")
        parts.append(f"![{slug} mobile](screens/{slug}-mobile.png)\n\n")
    (PRODUCT / "docs/design/DESIGN_README.md").write_text("\n".join(parts))


def _capture_screens(pages):
    """Render each design HTML to PNG. Claude Code runs scripts/screenshot.py (headless browser)."""
    import subprocess
    script = _ROOT / "scripts" / "screenshot.py"
    (PRODUCT / "docs/design/screens").mkdir(parents=True, exist_ok=True)
    for p in pages:
        for vp in ("desktop", "mobile"):
            html = PRODUCT / f"apps/web/design/{p['slug']}/{vp}.html"
            out = PRODUCT / f"docs/design/screens/{p['slug']}-{vp}.png"
            if html.exists():
                subprocess.run([sys.executable, str(script), str(html), str(out), vp],
                               capture_output=True)


def _topo_waves(chunks):
    """Dependency-ordered waves; within a wave chunks are independent."""
    done, waves, remaining = set(), [], list(chunks)
    while remaining:
        wave = [c for c in remaining if set(c.get("dependencies", [])) <= done]
        if not wave:  # break cycles defensively
            wave = remaining
        waves.append(wave)
        done |= {c["slug"] for c in wave}
        remaining = [c for c in remaining if c not in wave]
    return waves


def _run_local_deploy_and_checks():
    import subprocess
    sh = PRODUCT / "scripts/deploy_local.sh"
    if sh.exists():
        subprocess.run(["bash", str(sh)], cwd=PRODUCT)


def _consolidate_test_report(results):
    lines = ["# RoarPass — Test Report\n", "| Area | Passed | Failed | Defects |", "|---|---|---|---|"]
    for r in results:
        try:
            j = json.loads(r["json"]) if r["json"] else {}
        except json.JSONDecodeError:
            j = {}
        s = j.get("summary", {})
        lines.append(f"| {r['id']} | {s.get('passed','?')} | {s.get('failed','?')} | "
                     f"{len(j.get('defects', []))} |")
    (PRODUCT / "docs/TEST_REPORT.md").write_text("\n".join(lines))


def _run_demo_capture():
    import subprocess
    spec = PRODUCT / "demo/capture_spec.json"
    script = _ROOT / "scripts/demo_capture.py"
    if spec.exists():
        subprocess.run([sys.executable, str(script), str(spec)], cwd=PRODUCT, capture_output=True)


# ----------------------------- dynamic mode ---------------------------------
def run_dynamic(prd_path: str, st: dict):
    """Fully dynamic: a planner agent designs the whole plan, the executor runs it.
    Gate placement is entirely the planner's choice; only secret-safety + prohibited-action
    rails are enforced by the executor."""
    print("== Dynamic mode: planning ==")
    runlog.log_phase(-1, "planner", "running")
    # bootstrap the product repo + PRD first (same safe scaffold as static phase 0)
    run_phase0(st)
    prd = (PRODUCT / "docs" / "RoarPass_PRD.md").read_text()
    scope = _WF.get("build_scope", {})
    plan_raw = agent.run_agent(
        "planner",
        f"BUILD SCOPE:\n{json.dumps(scope, indent=2)}\n\nPRD:\n{prd}\n\n"
        "Design the full build plan as JSON per your output contract.",
        phase=-1, agent_id="planner")
    plan = json.loads(agent.parse_json(plan_raw))
    (PRODUCT / "docs").mkdir(parents=True, exist_ok=True)
    (PRODUCT / "docs" / "GENERATED_PLAN.json").write_text(json.dumps(plan, indent=2))
    runlog.log_phase(-1, "planner", "complete")
    st["plan"] = plan
    st["completed_phases"] = st.get("completed_phases", [])
    save_state(st)
    result = executor.run_plan(plan, st)
    if result == "complete":
        notify.send_done_notification("RoarPass dynamic build complete.")


def resume_dynamic(st: dict, approve: bool, feedback: str = ""):
    gate.record_decision(approve, feedback)
    gate.clear_gate()
    runlog.log_gate("approved" if approve else "revising")
    st["status"] = "running"
    # the paused phase wasn't marked complete; executor will resume from it
    result = executor.run_plan(st["plan"], st)
    if result == "complete":
        notify.send_done_notification("RoarPass dynamic build complete.")


# ----------------------------- driver ---------------------------------------
def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--prd")
    ap.add_argument("--resume", action="store_true")
    ap.add_argument("--approve", action="store_true")
    ap.add_argument("--revise")
    ap.add_argument("--revise-full", dest="revise_full")
    ap.add_argument("--status", action="store_true")
    ap.add_argument("--dynamic", action="store_true",
                    help="fully dynamic mode: a planner designs the whole plan, including gates")
    args = ap.parse_args()

    st = load_state()

    if args.status:
        print(json.dumps(st, indent=2))
        return

    if args.resume:
        if st.get("status") != "awaiting_approval":
            sys.exit("Nothing awaiting approval. Current state:\n" + json.dumps(st, indent=2))
        # dynamic resume if a generated plan exists
        if st.get("plan"):
            if args.approve:
                resume_dynamic(st, True)
            elif args.revise or args.revise_full:
                resume_dynamic(st, False, args.revise or args.revise_full)
            else:
                sys.exit("Use --approve or --revise \"...\" with --resume.")
            return
        # static resume
        if args.approve:
            gate.record_decision(True)
            gate.clear_gate()
            runlog.log_gate("approved")
            run_phase3(st)
            run_phase4(st)
            run_phase5(st)
        elif args.revise or args.revise_full:
            fb = args.revise or args.revise_full
            gate.record_decision(False, fb, full=bool(args.revise_full))
            gate.clear_gate()
            runlog.log_gate("revising")
            run_phase2(st, revise=fb, full=bool(args.revise_full))  # re-gates at the end
        else:
            sys.exit("Use --approve or --revise \"...\" with --resume.")
        return

    # fresh run
    if not args.prd:
        sys.exit("Provide --prd path on a fresh run.")
    runlog.reset()  # clear logs/transcripts only on a fresh run, not on --resume

    if args.dynamic:
        st = {"phase": -1, "status": "running", "chunks": None, "prd": args.prd, "dynamic": True}
        save_state(st)
        run_dynamic(args.prd, st)
        return

    st = {"phase": -1, "status": "init", "chunks": None, "prd": args.prd}
    save_state(st)
    run_phase0(st)
    run_phase1(st)
    run_phase2(st)  # halts at gate


if __name__ == "__main__":
    main()
